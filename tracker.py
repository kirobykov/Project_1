"""
Модуль для отслеживания настроения.
"""

class MoodTracker:
    """
    Класс для отслеживания настроения пользователя.
    """

    def __init__(self):
        """Инициализирует пустой словарь для хранения настроений."""
        self.moods = {}

    def add_mood(self, date, mood):
        """
        Добавляет настроение для указанной даты.

        :param date: Дата в формате 'YYYY-MM-DD'
        :param mood: Настроение (строка)
        """
        self.moods[date] = mood

    def get_mood(self, date):
        """
        Возвращает настроение для указанной даты.

        :param date: Дата в формате 'YYYY-MM-DD'
        :return: Настроение (строка) или сообщение об отсутствии данных
        """
        return self.moods.get(date, "Нет данных о настроении на эту дату.")
